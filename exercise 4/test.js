// function Basics
console.log("function Basics")
function add(a, b) {
    console.log(a + b);
}
add(5, 10);
add(3, 7);
add(9, 4);

// function expression
console.log("Function Expressions")
let add2 = function(a, b) {
    console.log(a + b);
}
add2(5, 12);
add2(2, 7);
add2(8, 4);

